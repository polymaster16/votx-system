function GalleryPage() {
  const theme = useTheme();
  const [activeStep, setActiveStep] = React.useState(0);
  const maxSteps = images.length;

  const handleNext = () => {
    setActiveStep((prevActiveStep) => prevActiveStep + 1);
  };

  const handleBack = () => {
    setActiveStep((prevActiveStep) => prevActiveStep - 1);
  };

  const handleStepChange = (step) => {
    setActiveStep(step);
  };

  return (
    <>
   
    <Box 
   ml={"auto"}
   mr={"auto"}
    sx={{ maxWidth: 500, flexGrow: 1 }}>
    
      <AutoPlaySwipeableViews
        axis={theme.direction === 'rtl' ? 'x-reverse' : 'x'}
        index={activeStep}
        onChangeIndex={handleStepChange}
        enableMouseEvents
      >
        {itemData.map((step, index) => (
          <div key={step.title}>
            {Math.abs(activeStep - index) <= 2 ? (
              <Box
                component="img"
                sx={{
                  height: "auto",
                  display: 'block',
                  maxWidth: "auto",
                  overflow: 'hidden',
                  width: '100%',
                }}
                src={step.img}
                alt={step.title}
              />
            ) : null}
          </div>
        ))}
        
      </AutoPlaySwipeableViews>

      
      <MobileStepper
      variant="progress"
        color="secondary"
        steps={maxSteps}
        position="static"
        activeStep={activeStep}
        nextButton={
          <Button
          color="secondary"
            size="small"
            onClick={handleNext}
            disabled={activeStep === maxSteps - 1}
          >
            
            {theme.direction === 'rtl' ? (
              <KeyboardArrowLeft />
            ) : (
              <KeyboardArrowRight />
            )}
          </Button>
        }
        backButton={
          <Button size="small"
          color="secondary" onClick={handleBack} disabled={activeStep === 0}>
            {theme.direction === 'rtl' ? (
              <KeyboardArrowRight color="secondary" />
            ) : (
              <KeyboardArrowLeft color="secondary" />
            )}
            
          </Button>
        }
      />
    </Box>


<ImageList 
 sx={{ width: 400, height: 450 }} cols={2} rowHeight={164}>
      {itemData.map((item) => (
        <ImageListItem key={item.img}>
          <img
            src={`${item.img}?w=164&h=164&fit=crop&auto=format`}
            srcSet={`${item.img}?w=164&h=164&fit=crop&auto=format&dpr=2 2x`}
            alt={item.title}
            loading="lazy"
          />
        </ImageListItem>
      ))}
    </ImageList>


    </>
  );
}

export default GalleryPage;
